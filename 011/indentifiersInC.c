#include<stdio.h>

int main () {
    
    int a = 10;
    int A = 20;

    printf("\nValue of a: %d", a);
    printf("\nValue of A: %d", A);

    printf("\n\n");

    printf("The compiler treates variable a and variable A differently.");
    printf("\nIt means Identifiers in C are case-sensitive.");

    printf("\n\n");
    
    return 0;

}

